#ifndef ARCH_ARM64_CONTEXT_H_
#define ARCH_ARM64_CONTEXT_H_

#include <cstdint>

#include "arch/arm64/system.h"

namespace arch {
namespace arm64 {

struct Context {
  using Spsr = arch::arm64::sys::SavedProcessStatusRegister;

  struct Registers {
    uint64_t x0;
    uint64_t x1;
    uint64_t x2;
    uint64_t x3;
    uint64_t x4;
    uint64_t x5;
    uint64_t x6;
    uint64_t x7;
    uint64_t x8;
    uint64_t x9;
    uint64_t x10;
    uint64_t x11;
    uint64_t x12;
    uint64_t x13;
    uint64_t x14;
    uint64_t x15;
    uint64_t x16;
    uint64_t x17;
    uint64_t x18;
    uint64_t x19;
    uint64_t x20;
    uint64_t x21;
    uint64_t x22;
    uint64_t x23;
    uint64_t x24;
    uint64_t x25;
    uint64_t x26;
    uint64_t x27;
    uint64_t x28;
    uint64_t x29;
    uint64_t x30;
  };

  void* translation_table;
  void* elr;
  Spsr spsr;
  void* sp;
  Registers registers;
};

static_assert(sizeof(Context) == ((31 * 8) + (4 * 8)));

}  // namespace arm64
}  // namespace arch

#endif  // ARCH_ARM64_CONTEXT_H_
